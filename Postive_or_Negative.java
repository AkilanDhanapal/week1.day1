package Given_Pos_or_Neg;

public class Postive_or_Negative {
	public static void main(String[] args) {
	int a = 35;
	if (a > 0) {
		System.out.println("Positive");
	}
	else if (a < 0) {
		System.out.println("Negative");
	}	
	else {
		System.out.println("Neither negative nor positive");
	}
	}
}
